get_real_floor <- function(n){

                  
          if (n == 1){
                    
                    print(0)
                    
          }else if (n > 1 && n < 13 ){
                    
                    print(n - 1)
                    
          }else if (n >= 13){
                    
                    print(n - 2)
                    
          }else if (n == 0){
                    
                    print(0)
                    
          }else if (n){
                    
                    print(n)
                    
          }
          
          
}